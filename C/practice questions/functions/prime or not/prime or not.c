#include <stdio.h>

void prime(int n) {
    int i, count = 0;
    if (n == 1) {
        printf("%d is not a prime number\n", n);
    } else {
        for (i = 2; i <= n / 2; i++) {
            if (n % i == 0) {
                printf("%d is not a prime number\n", n);
                count = 1;
                break;
            }
        }
        if (count == 0) {
            printf("%d is a prime number\n", n);
        }
    }












































































































































































































































    
}

int main() {
    int n;
    printf("Enter the number to check whether it is prime: ");
    scanf("%d", &n);
    prime(n);
    return 0;
}